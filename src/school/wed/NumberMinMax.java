package school.wed;

public class NumberMinMax {
    public static void main(String[] args) {
        System.out.printf("최소: %d, 최대: %d\n", Byte.MIN_VALUE, Byte.MAX_VALUE);
        System.out.printf("최소: %d, 최대: %d\n", Short.MIN_VALUE, Short.MAX_VALUE);
        System.out.printf("최소: %d, 최대: %d\n", Integer.MIN_VALUE, Integer.MAX_VALUE);
        System.out.printf("최소: %d, 최대: %d\n", Long.MIN_VALUE, Long.MAX_VALUE);
        System.out.printf("최소: %.0f, 최대: %.0f\n", Float.MIN_VALUE, Float.MAX_VALUE);
        System.out.printf("최소: %.0f, 최대: %.0f\n", Double.MIN_VALUE, Double.MAX_VALUE);
    }
}
